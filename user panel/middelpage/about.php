                        <h3>Overview</h3>
                   </div>
               </div>
               <div class="row">
                    <div class="col-md-12">
                       <p>
                           <img src="upload/ab1.jpg" align="right" hspace="30" vspace="10" />
                           Live like home hostel was established on 4th October 1953 in the meeting at Dr. Haribhai Gauda’s house. At that time, hostels have been established in Amreli, Bhavnagar, Jamnagar and Dhoraji. But all these hostels were only for school students. There was not any such type of facility for college students.
                       
                        </p>
                        <p>       Gujarat Education Society was instituted during this time and Engineering, Medical, Pharmacy, Commerce, Laws etc. courses were introduced at new colleges. So hostel facility in Ahmedabad was required. For this, land for hostel establishment was bought in 1955 and hostel was started in 1957 with small building of 8 rooms.
                        </p>
                        
                        <p>       As time went there was requirement for extra rooms so in 1959 extra 5 rooms, in 1962 new floor with 5 rooms and 4 rooms in 1968 were built. Finally in 1968, two storey building with 22 rooms was prepared for students who wanted to study in Ahmedabad. M. Patel engineering Company Ltd., Mumbai donated Rs.25, 000 and Shree Gokalbhai Darabar of Surat donated Rs.22,000. These were the main donations in the construction.
                        </p>
                        <p>       The building  of Hostel was ready but they didn’t have dining hall so students had to sit in the corridor to dine but they faced problems in rainy season. At t hat time our society was doing great economic progress.
                        </p>
                        
                        <p>
                            <img src="upload/ab4.jpg" align="left" hspace="30" vspace="10" />
                            For satisfying increasing needs of students, new floor’s construction was initiated on “VijyaDashmi” at 8 October 1981. At that time Shree Vrajlal (Nanubhai) Zinabhai Shiroya, Shree Nathala Jivarajbhai Devani, Shree Bhikhubhai Nanjibhai Padsala and Shree Hakamichandbhai Patel donated Rs.1, 11,111 each and Shree Mohanbhai Valjibhai Vadaliya donated Rs.40, 000 Besides this donation, Rs.11, 00,000 were announced as total donation
                        </p>
                        <p>
                                   Also from the very beginning girl’s education and empowerment had been the aim of our organization. So 1.17 acre land for girl’s hostel was bought near AEC Sola Cross Road, Naranpura. But this land was under reservation of AUDA’s old environmental be it area, so the governmental procedure  to relieve this land was started in 1992.

                        </p>
                        <p>
                                   Due to delay in that process, spirit of unity and tendency of donation were improved. On 12th November 1995 it was decided in the conference of board members to demolish old building and construct new high-rise building for better use of land. The blueprints of building designed under observation of planning committee were approved from Corporation. So old  building was demolished in April 1996 and construction of new building was initiated on 21st April 1996.

                        </p>
                        <p>
                                   Hard work of activists and cooperation of donors have paid off in the construction of the building. In this new building there are 160 rooms with capacity of 4 students each, well facilitated dining hall with capacity of 110 students, hall, kitchen, a large cultural hall, library, computer room, TV room, offices, parking, newly furnished another dining hall etc. Besides this building another new building for higher courses like engineering, medical, MBA, CA etc. with capacity of 220 students is also constructed.

                        </p>
                        <p>
                             After completing construction of boy’s hostel, Shree Vallabhbhai G. Kakadiya, Shree Ravjibhai P. Vasani, Shree Pareshbhai Pansuriya, Shree Gunavantbhai B. Sojitra, Shree Parshottambhai Gevariya, Shree Valjibhai Domadiya, Shree Kanubhai J. Patel, Shree Babubhai Sheladiya, Shree Nanubhai Parsana and Shree Bhikhubhai Sojitra all together started realizing a dream of hostel.

                        </p>
                        <p>
                            <img src="upload/ab3.jpg" align="right" hspace="30" vspace="10" />
                                   Shree Dirubhai Bhagat donated Rs.1.5cr for hostel. So Saurastra Patel Kelavani Mandal, Ahmedabad organized Shreemati Vijyaben Maganbhai Nagajibhai Bhagat Girls Hostel (accommodation of about 860 girls) was initiated by Chief Minister of Gujarat on 2nd January 2008 and was inaugurated by Chief Minister of Gujarat then and now Prime Minister of  India, Shree Narendra Modi.

                        </p>
                        <p>      Shree Babubhai Sheladiya, Shree Rambhai Sheladiya, Shree Nanubhai Parsana, Shree Himmatbhai Kasvala, Shree Vallabhbhai Kakadiya, Shree Ravjibhai Vasani, Shree Gunavantbhai Sojitra have placed their best efforts to complete this work in minimal time without worrying about their own businesses.
                        </p>
