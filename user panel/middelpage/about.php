                        <h3>L.L.H. Hostel for Students</h3>
                        <hr class="title-underline">
                        <p>
                           Live Like Home (LLH) is accomplishing wonderful task by providing opportunity to the students of through the medium of accommodation in huge city like Ahmedabad and encouraging them to pursue higher and better education. It aims at providing comfortable, hygienic accommodations with assured quality standards at affordable rates. All in all, the hostel life in LLH is one of the most enviable among the student community, providing students with conducive atmosphere for learning and personal development.
                       </p>
